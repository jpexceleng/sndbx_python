# The following demos basic math operators.

a = 2
b = 3

# addition
print(a + b) # 5

# subtraction
print(a - b) # -1

# divison
print(a / b) # 0.666666666666666
# Note how result is automatically converted to float type

# floor division (truncates fractional part of result)
print (a // b) # 0

# multiplication
print(a * b) # 6

# powers
print(a ** b) # 8

# modulus
print(a % b) # 2